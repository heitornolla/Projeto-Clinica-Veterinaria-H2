    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nome = jTextField1.getText().trim();
        String anoNascText = jTextField2.getText().trim();
    
        if (nome.isEmpty() || anoNascText.isEmpty() || jComboBox1.getSelectedItem() == null ||
                jComboBox1.getSelectedIndex() < 1 || jComboBox2.getSelectedIndex() < 1 
                || jComboBox3.getSelectedIndex() < 1) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos antes de criar o animal.",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            int anoNasc = Integer.parseInt(anoNascText);
            LocalDate dataAtual = LocalDate.now();
            int anoAtual = dataAtual.getYear();
            
            if (anoNasc > anoAtual || !anoNascText.matches("[0-9()-]*")) {
                JOptionPane.showMessageDialog(this, "Insira um ano de nascimento válido.",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
            }

            String sexo = "";
            if (jComboBox1.getSelectedIndex() == 1) {
                sexo = "Masculino";
            } else if (jComboBox1.getSelectedIndex() == 2) {
                sexo = "Feminino";
            }

            Animal animal = AnimalDAO.getInstance().create(nome, anoNasc, sexo,
                    jComboBox2.getSelectedIndex() - 1, jComboBox3.getSelectedIndex() + 0);

            jTextField1.setText("");
            jTextField2.setText("");
            clearComponents();
            this.myInitComponents();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "O ano de nascimento deve ser um número válido.",
                    "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
