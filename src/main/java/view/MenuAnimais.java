    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() >= 0) {

            Object value = jTable1.getValueAt(jTable1.getSelectedRow(), 0);

            if (value != null) {
                int idAnimal = Integer.parseInt(value.toString());
                List<Consulta> futurasConsultas = ConsultaDAO.getInstance().retrieveOngoingByAnimalId(idAnimal);
                
                if (!futurasConsultas.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Não foi possível deletar o animal.\n"
                            + "Motivo: O animal ainda fará consultas.", 
                            "Erro", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "CUIDADO!\n"
                    + "O animal não possui consultas futuras registradas, mas ao deletá-lo, todo seu histórico é perdido!\n"
                            + "Essa ação é irreversível. Digite OK para confirmar.\n", 
                    "Erro", JOptionPane.ERROR_MESSAGE);
                    String confirma = JOptionPane.showInputDialog(this, "Confirme deleção do animal:");
                    if ("OK".equals(confirma)) {
                       Animal aDeletar = AnimalDAO.getInstance().retrieveById(idAnimal);
                       AnimalDAO.getInstance().delete(aDeletar);
                       JOptionPane.showMessageDialog(this, "Animal deletado com sucesso.", 
                       "Aviso", JOptionPane.INFORMATION_MESSAGE);
                       initTable();
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nada foi selecionado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

