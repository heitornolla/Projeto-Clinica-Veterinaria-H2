    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() >= 0) {

            Object value = jTable1.getValueAt(jTable1.getSelectedRow(), 0);

            if (value != null) {
                int idVeterinario = Integer.parseInt(value.toString());
                List<Consulta> futurasConsultas = ConsultaDAO.getInstance().retrieveByOngoingAndVetId(idVeterinario);
                
                if (!futurasConsultas.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Não foi possível deletar o veterinário.\n"
                            + "Motivo: O veterinário possui consultas não finalizadas no sistema.", 
                            "Erro", JOptionPane.ERROR_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this, "CUIDADO!\n"
                    + "O veterinário não possui consultas futuras registradas, mas ao deletá-lo, todo seu histórico é perdido!\n"
                            + "Essa ação é irreversível. Digite OK para confirmar.\n", 
                    "Erro", JOptionPane.ERROR_MESSAGE);
                    String confirma = JOptionPane.showInputDialog(this, "Confirme deleção do veterinário:");
                    if ("OK".equals(confirma)) {
                       Veterinario aDeletar = VeterinarioDAO.getInstance().retrieveById(idVeterinario);
                       VeterinarioDAO.getInstance().delete(aDeletar);
                       JOptionPane.showMessageDialog(this, "Veterinário deletado com sucesso.", 
                       "Aviso", JOptionPane.INFORMATION_MESSAGE);
                       initTable();
                    } else {
                        JOptionPane.showMessageDialog(this, "Operação cancelada.", 
                       "Aviso", JOptionPane.INFORMATION_MESSAGE); 
                    }
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nada foi selecionado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

