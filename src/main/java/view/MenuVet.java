    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        String nome = jTextField1.getText().trim();
        String email = jTextField2.getText().trim();
        String telefone = jTextField3.getText().trim();
        
        if (nome.isEmpty() || email.isEmpty() || telefone.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!telefone.matches("[0-9()-]*")) {
            JOptionPane.showMessageDialog(this, "Atente-se ao formato correto do telefone!\n"
                    + "Formato correto: (00)91234-5678\n"
                    + "Sem espaços!",
            "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Preencha o email corretamente!\n",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (telefone.length() < 14) {
            JOptionPane.showMessageDialog(this, "Preencha o Telefone corretamente!\n"
                    + "Formato correto: (XX)91234-5678\n"
                    + "Sem espaços!",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Veterinario veterinario = VeterinarioDAO.getInstance().create(nome, 
                email, telefone);
        clearComponents();      
        initTable();
    }//GEN-LAST:event_jToggleButton1ActionPerformed

