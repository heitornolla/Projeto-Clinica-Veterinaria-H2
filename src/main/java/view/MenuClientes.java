    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (jTable1.getSelectedRow() >= 0) {

            Object value = jTable1.getValueAt(jTable1.getSelectedRow(), 0);

            if (value != null) {
                int idCliente = Integer.parseInt(value.toString());
                List<Animal> animais = AnimalDAO.getInstance().retrieveByClientId(idCliente);
                
                if (!animais.isEmpty()) {
                    JOptionPane.showMessageDialog(this, "Não foi possível deletar o cliente.\n"
                            + "Motivo: O cliente possui animais cadastrados no sistema.", 
                            "Erro", JOptionPane.ERROR_MESSAGE);
                } else {
                    Cliente aDeletar = ClienteDAO.getInstance().retrieveById(idCliente);
                    ClienteDAO.getInstance().delete(aDeletar);
                    JOptionPane.showMessageDialog(this, "Cliente deletado com sucesso.",
                            "Aviso", JOptionPane.INFORMATION_MESSAGE);
                    initTable();
                    }
                }
            } else {
            JOptionPane.showMessageDialog(this, "Nada foi selecionado", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

