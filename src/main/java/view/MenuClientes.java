    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nome = jTextField1.getText().trim();
        String endereco = jTextField2.getText().trim();
        String cep = jTextField4.getText().trim();
        String email = jTextField5.getText().trim();
        String telefone = jTextField3.getText().trim();

        
        if (nome.isEmpty() || endereco.isEmpty() || cep.isEmpty() || 
            email.isEmpty() || telefone.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos.",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!telefone.matches("[0-9()-]*")) {
            JOptionPane.showMessageDialog(this, "Atente-se ao formato correto do telefone!\n"
                    + "Formato correto: (00)91234-5678\n"
                    + "Sem espaços!",
            "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (telefone.length() < 14) {
            JOptionPane.showMessageDialog(this, "Preencha o Telefone corretamente!\n"
                    + "Formato correto: (00)91234-5678\n"
                    + "Sem espaços!",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!cep.matches("[0-9()-]*")) {
            JOptionPane.showMessageDialog(this, "Atente-se ao formato correto do cep!\n"
                    + "Formato correto: 12345-678\n",
            "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (cep.length() < 9) {
            JOptionPane.showMessageDialog(this, "Preencha o CEP corretamente!\n"
                    + "Formato correto: 12345-678",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (!email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Preencha o email corretamente!\n",
                    "Erro", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        Cliente cliente = ClienteDAO.getInstance().create(nome, endereco, 
                cep, email, telefone);
        clearComponents();
        initTable();
    }//GEN-LAST:event_jButton1ActionPerformed

